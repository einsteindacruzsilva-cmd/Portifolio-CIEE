function resolveImageCandidates(fileName) {
  if (!fileName) return [];
  return [`./images/${fileName}`, `./public/images/${fileName}`];
}

function loadImageWithFallback(imgElement, onMissing, fileName, altText) {
  const candidates = resolveImageCandidates(fileName);
  let index = 0;

  function tryNext() {
    if (index >= candidates.length) {
      onMissing();
      imgElement.removeAttribute("src");
      return;
    }
    imgElement.src = candidates[index];
    imgElement.alt = altText;
    index += 1;
  }

  imgElement.addEventListener("error", tryNext);
  tryNext();
}

function loadPortrait(imgElement, container, fileName, memberName) {
  imgElement.addEventListener("load", () => {
    container.classList.remove("is-placeholder");
  });

  loadImageWithFallback(
    imgElement,
    () => container.classList.add("is-placeholder"),
    fileName,
    `${memberName} - imagem 3D do portfolio`
  );
}

function loadMediaImage(frameElement, imgElement, fileName, altText) {
  imgElement.addEventListener("load", () => {
    frameElement.classList.remove("is-placeholder");
    frameElement.classList.add("has-image");
  });

  loadImageWithFallback(
    imgElement,
    () => {
      frameElement.classList.add("is-placeholder");
      frameElement.classList.remove("has-image");
    },
    fileName,
    altText
  );
}

